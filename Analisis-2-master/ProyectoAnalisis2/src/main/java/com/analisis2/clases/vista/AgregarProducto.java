    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("conexion");

        if (jComboBox1.getSelectedItem() != null && !jTextField2.getText().equals("")  && !jTextField3.getText().equals("") && !jTextField4.getText().equals("")            )
        {
            Producto p1 = new Producto();
            Marca m1 = (Marca) jComboBox1.getSelectedItem();

            p1.setNombre(jTextField2.getText());
            p1.setMedida(jTextField3.getText());
            p1.setDescripcion(jTextField4.getText());
            p1.setExistencia(0);
            p1.setMarcaidMarca(m1);
            p1.setPrecio(Float.parseFloat(jTextField5.getText()));

            ProductoJpaController productoController = new ProductoJpaController(emf);

            try
            {
                productoController.create(p1);

                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");

                JOptionPane.showMessageDialog(null,"Producto registrado con exito");
            } catch (Exception ex) {
                Logger.getLogger(IncersionInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

