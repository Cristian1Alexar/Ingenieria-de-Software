        float ganancia = Float.parseFloat(jTextField2.getText());
        int existencia = Integer.parseInt(jTextField3.getText());
        
        ganancia = ganancia + precio;
        
        IncersionArreglo i = new IncersionArreglo(grupo, grupo.getPrecio());
        i.crearArreglo(nombre, ganancia, existencia);
        
        JOptionPane.showMessageDialog(null,"Factura registrada con exito");
        
        MenuPrincipal m = new MenuPrincipal();
        m.setVisible(true);
        this.dispose();
